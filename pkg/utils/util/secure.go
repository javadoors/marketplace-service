package util

import (
	"encoding/base64"
)

// DecodeBase64String decode base64 string
func DecodeBase64String(input string) (string, error) {
	decoded, err := base64.StdEncoding.DecodeString(input)
	if err != nil {
		return "", err
	}
	return string(decoded), nil
}
